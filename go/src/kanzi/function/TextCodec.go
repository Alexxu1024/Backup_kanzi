func NewTextCodec() (*TextCodec, error) {
	return NewTextCodecWithArgs(nil, TC_LOG_HASHES_SIZE, TC_DICTIONARY_SIZE, TC_DEFAULT_ESCAPE_TOKEN1, TC_DEFAULT_ESCAPE_TOKEN2)
}

func NewTextCodecWithArgs(dict []byte, logHashSize int, dictSize int, delimiter1 byte, delimiter2 byte) (*TextCodec, error) {
	if isText(delimiter1) {
		return nil, errors.New("Invalid delimiter parameter 1 (cannot be a text symbol)")
	}

	if isText(delimiter2) {
		return nil, errors.New("Invalid delimiter parameter 2 (cannot be a text symbol)")
	}

	if delimiter1 == delimiter2 {
		return nil, errors.New("The 2 delimiters must be different symbols")
	}

	if logHashSize < 10 || logHashSize > 28 {
		return nil, errors.New("The hash table size log must be in [10..28]")
	}

	if dictSize < TC_STATIC_DICT_WORDS || dictSize > 32768 {
		return nil, fmt.Errorf("The number of words in the dictionary must be in [%v..32767]", TC_STATIC_DICT_WORDS)
	}

	this := new(TextCodec)
	this.escape1 = delimiter1 // dictionary word preceded by space symbol
	this.escape2 = delimiter2 // toggle upper/lower case of first word char
	this.logHashSize = uint(logHashSize)
	this.dictMap = make([]*DictEntry, 1<<this.logHashSize)
	this.dictList = make([]DictEntry, dictSize)
	this.hashMask = int32(1<<this.logHashSize) - 1
	nbWords := 0

	// Replace default dictionary ?
	if dict != nil && &dict != &TC_DICT_EN_1024 {
		// Keep at least 20% space for dynamic dictionary
		nbWords = createDictionary(dict, this.dictList, dictSize*4/5, 0)
	} else {
		size := len(TC_STATIC_DICTIONARY)

		if size >= dictSize {
			size = dictSize
		}

		copy(this.dictList, TC_STATIC_DICTIONARY[0:size])
		nbWords = TC_STATIC_DICT_WORDS
	}

	// Populate hash map
	for i := 0; i < nbWords; i++ {
		e := this.dictList[i]
		this.dictMap[e.hash&this.hashMask] = &e
	}

	// Pre-allocate all dictionary entries
	for i := nbWords; i < dictSize-2; i++ {
		this.dictList[i] = DictEntry{buf: nil, pos: -1, hash: 0, idx: int16(i), length: int16(0)}
	}

	// Add special entries at end of map
	this.dictList[dictSize-1] = DictEntry{buf: []byte{this.escape1}, pos: 0, hash: 0, idx: int16(dictSize - 1), length: int16(1)}
	this.dictList[dictSize-2] = DictEntry{buf: []byte{this.escape2}, pos: 0, hash: 0, idx: int16(dictSize - 2), length: int16(1)}
	this.staticDictSize = int16(nbWords)
	return this, nil
}

func initDelimiterChars() []bool {
	res := make([]bool, 256)

	for i := range res {
		if (i >= ' ') && (i <= '/') { // [ !"#$%&'()*+,-./]
			res[i] = true
		} else if (i >= ':') && (i <= '?') { // [:;<=>?]
			res[i] = true
		} else {
			switch i {
			case '\n':
				fallthrough
			case '\r':
				fallthrough
			case '\t':
				fallthrough
			case '_':
				fallthrough
			case '|':
				fallthrough
			case '{':
				fallthrough
			case '}':
				fallthrough
			case '[':
				fallthrough
			case ']':
				res[i] = true
			default:
				res[i] = false
			}
		}
	}

	return res
}

func initTextChars() []bool {
	res := make([]bool, 256)

	for i := range res {
		res[i] = isLowerCase(byte(i)) || isUpperCase(byte(i))
	}

	return res
}

func (this *TextCodec) Forward(src, dst []byte) (uint, uint, error) {
	if src == nil {
		return uint(0), uint(0), errors.New("Invalid nil source buffer")
	}

	if dst == nil {
		return uint(0), uint(0), errors.New("Invalid nil destination buffer")
	}

	if kanzi.SameByteSlices(src, dst, false) {
		return 0, 0, errors.New("Input and output buffers cannot be equal")
	}

	count := len(src)

	if n := this.MaxEncodedLen(count); len(dst) < n {
		return 0, 0, fmt.Errorf("Output buffer is too small - size: %d, required %d", len(dst), n)
	}

	srcIdx := 0
	dstIdx := 0

	if count <= 1 {
		if count > 0 {
			dst[dstIdx] = src[srcIdx]
			srcIdx++
			dstIdx++
		}

		return uint(srcIdx), uint(dstIdx), nil
	}

	srcEnd := count
	dstEnd := len(dst)
	dstEnd2 := len(dst) - 2
	var anchor int // previous delimiter

	if isText(src[srcIdx]) {
		anchor = -1
	} else {
		anchor = 0
	}

	endWordRef := ^anchor
	emitAnchor := 0 // never negative
	words := this.staticDictSize
	dynamicDictSize := int16(TC_DICTIONARY_SIZE - 3)
	h1 := TC_HASH1
	h2 := TC_HASH1
	isFirstWordChar := true

	for (srcIdx < srcEnd) && (dstIdx < dstEnd) {
		cur := src[srcIdx]

		if isText(cur) {
			// Compute hashes
			// h1 -> hash of word chars
			// h2 -> hash of word chars with first char case flipped
			if isFirstWordChar == true {
				var caseFlag int32

				if isUpperCase(cur) {
					caseFlag = 32
				} else {
					caseFlag = -32
				}

				h1 = h1*TC_HASH1 ^ (int32(cur) * TC_HASH2)
				h2 = h2*TC_HASH1 ^ ((int32(cur) + caseFlag) * TC_HASH2)
				isFirstWordChar = false
			} else {
				h := int32(cur) * TC_HASH2
				h1 = h1*TC_HASH1 ^ h
				h2 = h2*TC_HASH1 ^ h
			}

			srcIdx++
			continue
		}

		mustEmit := true

		if (srcIdx > anchor+2) && (isDelimiter(cur) || cur == this.escape1 || cur == this.escape2) { // At least 2 letters
			// Check word in dictionary
			length := srcIdx - anchor - 1
			pe1 := this.dictMap[h1&this.hashMask]
			pe2 := this.dictMap[h2&this.hashMask]

			// Hash collision (quick check) ?
			if (pe1 != nil) && (pe1.length != int16(length) || pe1.hash != h1) {
				pe1 = nil
			}

			// Hash collision (quick check)  ?
			if (pe2 != nil) && (pe2.length != int16(length) || pe2.hash != h2) {
				pe2 = nil
			}

			var pe *DictEntry

			if pe2 == nil {
				pe = pe1
			} else {
				pe = pe2
			}

			if pe != nil {
				// Hash collision (full check) ?
				if !sameWords(pe.buf[pe.pos+1:pe.pos+length], src[anchor+2:anchor+2+length]) {
					pe = nil
				}
			}

			if pe == nil {
				// Word not found in the dictionary: add it if at least 3 chars
				if length > 2 {
					// Dictionary full ? Reset index to end of static dictionary
					if words > dynamicDictSize {
						words = this.staticDictSize
					}

					pe := &this.dictList[words]

					if pe.idx >= this.staticDictSize && pe.idx <= dynamicDictSize && length < 32768 {
						// Evict and reuse old entry
						this.dictMap[pe.hash&this.hashMask] = nil
						pe.buf = src
						pe.pos = anchor + 1
						pe.hash = h1
						pe.idx = words
						pe.length = int16(length)
					}

					// Update hash map
					this.dictMap[h1&this.hashMask] = pe
					words++
				}
			} else {
				// Word found in the dictionary
				// Skip space if only delimiter between 2 word references
				if endWordRef != anchor || src[anchor] != ' ' {
					dstIdx += this.emit(src[emitAnchor:anchor+1], dst[dstIdx:dstEnd])
				}

				if dstIdx >= dstEnd2 {
					break
				}

				// Emit escape
				if pe2 == nil {
					dst[dstIdx] = this.escape1
				} else {
					dst[dstIdx] = this.escape2
				}

				dstIdx++

				// Emit word index (varint 7 bits + 8 bits)
				if pe.idx >= 128 {
					dst[dstIdx] = byte((pe.idx & 0x7F) | 0x80)
					dst[dstIdx+1] = byte(pe.idx >> 7)
					dstIdx += 2
				} else {
					dst[dstIdx] = byte(pe.idx)
					dstIdx++
				}

				endWordRef = srcIdx
				mustEmit = false
			}
		}

		if mustEmit == true {
			// Emit all symbols since last delimiter
			dstIdx += this.emit(src[emitAnchor:srcIdx], dst[dstIdx:dstEnd])
		}

		// Reset delimiter position
		anchor = srcIdx
		emitAnchor = anchor
		srcIdx++
		isFirstWordChar = true
		h1 = TC_HASH1
		h2 = TC_HASH1
	}

	// Emit last symbols
	dstIdx += this.emit(src[emitAnchor:srcEnd], dst[dstIdx:dstEnd])

	err := error(nil)

	if srcIdx != srcEnd {
		err = fmt.Errorf("Forward transform failed. Source index: %v, expected: %v", srcIdx, srcEnd)
	}

	return uint(srcIdx), uint(dstIdx), err
}

func (this *TextCodec) emit(src, dst []byte) int {
	if len(src) == 0 {
		return 0
	}

	if 3*len(src) < len(dst) {
		return this.emit1(src, dst)
	} else {
		return this.emit2(src, dst)
	}
}

func (this *TextCodec) emit1(src, dst []byte) int {
	// Fast path
	dstIdx := 0

	if src[0] == this.escape1 {
		// Emit special word
		dst[dstIdx] = this.escape1
		dst[dstIdx+1] = byte(0xFF)
		dst[dstIdx+2] = byte(0xFF)
		dstIdx += 3
	} else if src[0] == this.escape2 {
		// Emit special word
		dst[dstIdx] = this.escape1
		dst[dstIdx+1] = byte(0xFE)
		dst[dstIdx+2] = byte(0xFF)
		dstIdx += 3
	} else {
		dst[dstIdx] = src[0]
		dstIdx++
	}

	for i := 1; i < len(src); i++ {
		dst[dstIdx] = src[i]
		dstIdx++
	}

	return dstIdx
}

func (this *TextCodec) emit2(src, dst []byte) int {
	// Slow path
	dstIdx := 0
	dstEnd := len(dst)

	for i := range src {
		if src[i] == this.escape1 {
			if dstIdx+3 > dstEnd {
				break
			}

			// Emit special word
			dst[dstIdx] = this.escape1
			dst[dstIdx+1] = byte(0xFF)
			dst[dstIdx+2] = byte(0xFF)
			dstIdx += 3
		} else if src[i] == this.escape2 {
			if dstIdx+3 > dstEnd {
				break
			}

			// Emit special word
			dst[dstIdx] = this.escape1
			dst[dstIdx+1] = byte(0xFE)
			dst[dstIdx+2] = byte(0xFF)
			dstIdx += 3
		} else {
			if dstIdx+1 > dstEnd {
				break
			}

			dst[dstIdx] = src[i]
			dstIdx++
		}
	}

	return dstIdx
}

func sameWords(buf, src []byte) bool {
	// Skip first position (same result)
	for i := range buf {
		if buf[i] != src[i] {
			// Hash collision
			return false
		}
	}

	return true
}

func (this *TextCodec) Inverse(src, dst []byte) (uint, uint, error) {
	if src == nil {
		return uint(0), uint(0), errors.New("Invalid nil source buffer")
	}

	if dst == nil {
		return uint(0), uint(0), errors.New("Invalid nil destination buffer")
	}

	if kanzi.SameByteSlices(src, dst, false) {
		return 0, 0, errors.New("Input and output buffers cannot be equal")
	}

	srcIdx := 0
	dstIdx := 0
	count := len(src)

	if count <= 1 {
		if count > 0 {
			dst[dstIdx] = src[srcIdx]
			srcIdx++
			dstIdx++
		}

		return uint(srcIdx), uint(dstIdx), nil
	}

	srcEnd := len(src)
	dstEnd := len(dst)
	anchor := -1
	words := this.staticDictSize
	dynamicDictSize := int16(TC_DICTIONARY_SIZE - 3)
	wordRun := false
	err := error(nil)

	for (srcIdx < srcEnd) && (dstIdx < dstEnd) {
		cur := src[srcIdx]
		srcIdx++

		if isText(cur) {
			dst[dstIdx] = cur
			dstIdx++
			continue
		}

		if (srcIdx > anchor+3) && (cur == this.escape1 || cur == this.escape2 || isDelimiter(cur)) { // At least 2 letters
			// Create hash
			h := TC_HASH1
			length := int16(srcIdx - anchor - 2)

			for i := 1; i <= int(length); i++ {
				h = h*TC_HASH1 ^ int32(src[anchor+i])*TC_HASH2
			}

			// Lookup word in dictionary
			pe := this.dictMap[h&this.hashMask]

			// Hash collision ?
			if (pe != nil) && (pe.length != length || pe.hash != h) {
				pe = nil
			}

			if pe == nil {
				// Word not found in the dictionary: add it if at least 3 letters
				if length > 2 {
					// Dictionary full ?
					if words > dynamicDictSize {
						words = this.staticDictSize
					}

					pe = &this.dictList[words]

					if pe.idx >= this.staticDictSize && pe.idx <= dynamicDictSize {
						// Evict and reuse old entry
						this.dictMap[pe.hash&this.hashMask] = nil
						pe.buf = src
						pe.pos = anchor + 1
						pe.hash = h
						pe.idx = words
						pe.length = length
					}

					this.dictMap[h&this.hashMask] = pe
					words++
				}
			}
		}

		if (cur == this.escape1) || (cur == this.escape2) {
			// Word in dictionary
			// Read word index (varint 7 bits + 8 bits)
			idx := int(src[srcIdx] & 0xFF)
			srcIdx++

			if idx&0x80 != 0 {
				idx = (idx & 0x7F) | (int(src[srcIdx]&0xFF) << 7)
				srcIdx++

				if idx >= TC_DICTIONARY_SIZE {
					break
				}
			}

			pe := &this.dictList[idx]

			// Sanity check
			if (pe.pos < 0) || (dstIdx+int(pe.length) >= dstEnd) {
				err = fmt.Errorf("Invalid input data")
				break
			}

			// Add space if only delimiter between 2 words (2nd word in dictionary)
			if (wordRun == true) && (idx <= int(dynamicDictSize)) {
				dst[dstIdx] = ' '
				dstIdx++
			}

			// Emit word
			flag := byte(0)

			// Flip case of first character
			if cur == this.escape2 {
				if isUpperCase(pe.buf[pe.pos]) {
					flag = 32
				} else {
					flag = 256 - 32
				}
			}

			dst[dstIdx] = pe.buf[pe.pos] + flag
			dstIdx++

			buf := pe.buf[pe.pos+1 : pe.pos+int(pe.length)]

			for n := range buf {
				dst[dstIdx] = buf[n]
				dstIdx++
			}

			if idx <= int(dynamicDictSize) {
				// Regular word entry
				wordRun = true
				anchor = srcIdx
			} else {
				// Escape entry
				wordRun = false
				anchor = srcIdx - 1
			}
		} else {
			wordRun = false
			anchor = srcIdx - 1
			dst[dstIdx] = cur
			dstIdx++
		}
	}

	if (err == nil) && (srcIdx != srcEnd) {
		err = fmt.Errorf("Forward transform failed. Source index: %v, expected: %v", srcIdx, srcEnd)
	}

	return uint(srcIdx), uint(dstIdx), err
}

func (this TextCodec) MaxEncodedLen(srcLen int) int {
	// Space needed by destination buffer could be 3 x srcLength (if input data
	// is all delimiters). Limit to 1 x srcLength and ket the caller deal with
	// a failure when the output is not smaller than the input
	return srcLen
}

// Create dictionary from array of words
func createDictionary(words []byte, dict []DictEntry, maxWords, startWord int) int {
	anchor := -1
	h := TC_HASH1
	nbWords := startWord

	for i := 0; (i < len(words)) && (nbWords < maxWords); i++ {
		cur := words[i]

		if isText(cur) {
			h = h*TC_HASH1 ^ int32(cur)*TC_HASH2
			continue
		}

		if isDelimiter(cur) && (nbWords < maxWords) && (i >= anchor+2) { // At least 2 letters
			dict[nbWords] = DictEntry{buf: words, pos: anchor + 1, hash: h, idx: int16(nbWords), length: int16(i - anchor - 1)}
			nbWords++
		}

		anchor = i
		h = TC_HASH1
	}

	return nbWords
}

func isText(val byte) bool {
	return TC_TEXT_CHARS[val&0xFF]
}

func isLowerCase(val byte) bool {
	return (val >= 'a') && (val <= 'z')
}

func isUpperCase(val byte) bool {
	return (val >= 'A') && (val <= 'Z')
}

func isDelimiter(val byte) bool {
	return TC_DELIMITER_CHARS[val&0xFF]
}

func unpackDictionary(dict []byte) []byte {
	buf := make([]byte, len(dict)*2)
	d := 0
	val := 0

	// Unpack 3 bytes into 4 6-bit symbols
	for i := range dict {
		val = (val << 8) | int(dict[i]&0xFF)

		if (i % 3) == 2 {
			for ii := 18; ii >= 0; ii -= 6 {
				c := (val >> uint(ii)) & 0x3F

				if c >= 32 {
					buf[d] = ' '
					d++
				}

				c &= 0x1F

				// Ignore padding symbols (> 26 and <= 31)
				if c <= 26 {
					buf[d] = byte(c + 'a')
					d++
				}
			}

			val = 0
		}
	}

	buf[d] = ' ' // End
	res := make([]byte, d)
	copy(res[0:], buf[1:d+1])
	return res
}
